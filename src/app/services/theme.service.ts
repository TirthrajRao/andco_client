@Injectable({
  providedIn: 'root'
})
export class ThemeService {

  constructor() { }

  toggleDefault() {
    this.setTheme(defaults)
  }

  toggleFloral() {
    this.setTheme(floral)
  }

  toggleWood() {
    this.setTheme(wood)
  }

  toggleMarble() {
    this.setTheme(marble)
  }

  toggleOrigami() {
    this.setTheme(origami)
  }

  toggleClassic() {
    this.setTheme(classic)
  }

  toggleLines() {
    this.setTheme(lines)
  }

  toggleLuxury() {
    this.setTheme(luxury);
  }

  toggleInstruments() {
    this.setTheme(instruments);
  }




  private setTheme(theme: {}) {
    Object.keys(theme).forEach(k =>
      document.documentElement.style.setProperty(`--${k}`, theme[k])
    );
  }

}
